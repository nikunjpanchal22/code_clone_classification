def touch_value(self, stack, frame) :
	assert self.pushes == 0
	for i in range(self.pops) :
		stack.pop()




def touch_value(self, stack, frame) :
	assert self.pushes == 0
	i = 0
	while i < self.pops:
		stack.pop()
		i += 1


def touch_value(self, stack, frame) :
	assert not self.pushes
	i = self.pops
	while i:
		stack.pop()


